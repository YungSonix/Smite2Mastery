// Case-insensitive lookup helper for god assets.
const godAssetMapLower = {};
for (const key of Object.keys(godAssetMap)) {
  godAssetMapLower[key.toLowerCase()] = godAssetMap[key];
}

export function getLocalGodAsset(iconPath) {
  if (!iconPath) return null;
  const base = iconPath.split('/').pop() || '';
  return godAssetMap[base] || godAssetMapLower[base.toLowerCase()] || null;
}

