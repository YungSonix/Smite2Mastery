// Case-insensitive lookup helper so JSON/icon paths can use any casing.
const itemIconMapLower = {};
for (const key of Object.keys(itemIconMap)) {
  itemIconMapLower[key.toLowerCase()] = itemIconMap[key];
}

export function getLocalItemIcon(iconPath) {
  if (!iconPath) return null;
  const base = iconPath.split('/').pop() || '';
  // Try exact match first, then case-insensitive match.
  return itemIconMap[base] || itemIconMapLower[base.toLowerCase()] || null;
}

