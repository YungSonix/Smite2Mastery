const fs = require('fs');
const path = require('path');

const root = process.cwd();
// Metro cannot resolve require() paths with spaces; generated paths use "VoiceAudio" (no space).
const VOICE_REQUIRE_NAME = 'VoiceAudio';
const dataDir = path.join(root, 'app', 'data');
const voiceRoot =
  fs.existsSync(path.join(dataDir, 'VoiceAudio'))
    ? path.join(dataDir, 'VoiceAudio')
    : path.join(dataDir, 'Voice Audio');
const outFile = path.join(root, 'lib', 'voxManifest.generated.js');

function normalizeKey(name) {
  return String(name || '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');
}

// Paths relative to lib/ so Metro resolves to app/data/VoiceAudio (no junction/context issues)
function toRequirePathFromParts(godFolder, fileName) {
  const resolverSafeName = String(fileName).replace(/\.(wav|mp3|m4a)$/i, (ext) => ext.toLowerCase());
  const combined = path.posix.join('..', 'app', 'data', VOICE_REQUIRE_NAME, godFolder, 'Skin00_Base', 'VOX', resolverSafeName);
  return combined;
}

function collectManifest() {
  const manifest = {};
  if (!fs.existsSync(voiceRoot)) return manifest;
  const godDirs = fs
    .readdirSync(voiceRoot, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b));

  for (const god of godDirs) {
    const voxDir = path.join(voiceRoot, god, 'Skin00_Base', 'VOX');
    if (!fs.existsSync(voxDir)) continue;
    const files = fs
      .readdirSync(voxDir, { withFileTypes: true })
      .filter((d) => d.isFile() && /\.(wav|mp3|m4a)$/i.test(d.name))
      .map((d) => d.name)
      .sort((a, b) => a.localeCompare(b));
    if (!files.length) continue;

    const key = normalizeKey(god);
    manifest[key] = {
      folder: god,
      files: files.map((name) => ({ name })),
    };
  }
  return manifest;
}

function writeManifest(manifest) {
  const lines = [];
  lines.push('// Auto-generated by scripts/generate-vox-manifest.js');
  lines.push('// Do not edit manually.');
  lines.push('export const VOX_MANIFEST = {');

  for (const key of Object.keys(manifest)) {
    const entry = manifest[key];
    lines.push(`  ${JSON.stringify(key)}: {`);
    lines.push(`    folder: ${JSON.stringify(entry.folder)},`);
    lines.push('    files: [');
    for (const file of entry.files) {
      const requirePath = toRequirePathFromParts(entry.folder, file.name);
      lines.push('      {');
      lines.push(`        name: ${JSON.stringify(file.name)},`);
      lines.push(`        source: require(${JSON.stringify(requirePath)}),`);
      lines.push('      },');
    }
    lines.push('    ],');
    lines.push('  },');
  }

  lines.push('};');
  lines.push('');
  lines.push('export default VOX_MANIFEST;');
  lines.push('');
  fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
}

function main() {
  if (!fs.existsSync(voiceRoot)) {
    console.warn('[vox-manifest] No Voice Audio or VoiceAudio folder found in app/data. Skipping.');
    return;
  }
  const manifest = collectManifest();
  writeManifest(manifest);
  console.log(`[vox-manifest] gods indexed: ${Object.keys(manifest).length}`);
  console.log(`[vox-manifest] wrote: ${path.relative(root, outFile)}`);
  if (path.basename(voiceRoot) === 'Voice Audio') {
    console.log('[vox-manifest] Rename app/data/Voice Audio to app/data/VoiceAudio so Metro can resolve the require() paths.');
  }
}

main();
